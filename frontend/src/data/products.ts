// ─── Filter logic ─────────────────────────────────────────────────────────────
export function filterProducts(products: ProductEntry[], filter: string): ProductEntry[] {
  if (filter === 'all') return products
  if (filter === 'easy') return products.filter((p) => p.difficulty === '🥉 入门')
  if (filter === 'medium') return products.filter((p) => p.difficulty === '🥈 中等')
  if (filter === 'hard') return products.filter((p) => p.difficulty === '🥇 有门槛')

  const group = CATEGORY_GROUPS.find((g) => g.value === filter && g.chapter)
  if (group && group.chapter) {
    const chapters = group.chapter.split(',')
    return products.filter((p) => chapters.includes(p.hsChapter))
  }

  return products.filter((p) => filter.split(',').includes(p.hsChapter))
}
