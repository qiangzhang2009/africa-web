export const CATEGORY_GROUPS = DIFFICULTY_GROUPS  // 保留旧名

export function filterProductsLegacy(products: Product[], filter: string): Product[] {
  if (filter === 'all') return products
  if (filter === 'not-zero') return products.filter(p => !p.zeroTariff)
  return products.filter(p => p.category === filter)
}

// ── 辅助函数 ─────────────────────────────────────────────────────────────────
export function findCategoryL1ByValue(value: string): CategoryNode | undefined {
  return CATEGORY_TREE.find(c => c.value === value)
}

export function findCategoryL2ByValue(value: string): CategoryNode | undefined {
  for (const l1 of CATEGORY_TREE) {
    if (l1.children) {
      const found = l1.children.find(c => c.value === value)
      if (found) return found
    }
  }
  return undefined
}

export function findRegionByValue(value: string): RegionGroup | undefined {
  return REGION_GROUPS.find(r => r.value === value)
}
