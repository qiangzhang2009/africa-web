// ── 多维筛选函数 ──────────────────────────────────────────────────────────────
export function filterProducts(
  products: Product[],
  difficulty: string,
  categoryL1: string,   // 一级分类
  categoryL2: string,   // 二级分类
  region: string,       // 地区分组
  searchText: string,
): Product[] {
  let result = products

  // 难度筛选
  if (difficulty === 'not-zero') {
    result = result.filter(p => !p.zeroTariff)
  } else if (difficulty !== 'all') {
    result = result.filter(p => p.category === difficulty)
  }

  // 一级分类筛选
  if (categoryL1 !== 'all') {
    if (categoryL1 === 'not-zero-ref') {
      result = result.filter(p => p.category === 'not-zero')
    } else {
      result = result.filter(p => p.hsChapter === categoryL1)
    }
  }

  // 二级分类筛选
  if (categoryL2 !== 'all') {
    result = result.filter(p => p.hsChapterSub === categoryL2)
  }

  // 地区筛选
  if (region !== 'all') {
    if (region === 'east-west') {
      result = result.filter(p =>
        p.regions.includes('east-africa') || p.regions.includes('west-africa')
      )
    } else if (region === 'mineral-rich') {
      result = result.filter(p =>
        p.regions.includes('southern-africa') || p.regions.includes('central-africa')
      )
    } else if (region === 'textile-zone') {
      result = result.filter(p =>
        p.regions.includes('north-africa') || p.regions.includes('west-africa')
      )
    } else {
      result = result.filter(p => p.regions.includes(region))
    }
  }

  // 关键词搜索
  if (searchText.trim()) {
    const q = searchText.trim().toLowerCase()
    result = result.filter(p =>
      p.name.toLowerCase().includes(q) ||
      p.nameEn.toLowerCase().includes(q) ||
      p.hsCode.includes(q) ||
      p.originCountries.some(c => c.toLowerCase().includes(q))
    )
  }

  return result
}

// ── 计算分类计数 ──────────────────────────────────────────────────────────────
export function countByCategoryL1(products: Product[]): Record<string, number> {
  const counts: Record<string, number> = {}
  for (const p of products) {
    counts[p.hsChapter] = (counts[p.hsChapter] || 0) + 1
  }
  return counts
}

